#include "sys.h"
/*******************************************************************************
* 函数名 : 系统自定义延时函数
* 函数功能 : 延时时间（ms）
* 输入参数 : count
* 输出: 无
*******************************************************************************/
void delay_ms(int count)  // /* X1ms */
{
	while(count>0){
		u8 i, j;
		_nop_();
		i = 2;
		j = 199;
		do
		{
			while (--j);
		} while (--i);
		count--;
	}
}
